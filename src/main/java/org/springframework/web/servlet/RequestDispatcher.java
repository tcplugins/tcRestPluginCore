	public RequestDispatcher(
			ApplicationContext applicationContext, 
			RequestMappingHandlerAdapter requestMappingHandlerAdapter) {
		this.myRequestMappingHandlerAdapter = requestMappingHandlerAdapter;
		
		// Find all HandlerMappings in the ApplicationContext, including ancestor contexts.
		Map<String, HandlerMapping> matchingBeans =
				BeanFactoryUtils.beansOfTypeIncludingAncestors(applicationContext, HandlerMapping.class, true, false);
		if (!matchingBeans.isEmpty()) {
			this.handlerMappings = new ArrayList<HandlerMapping>(matchingBeans.values());
			// We keep HandlerMappings in sorted order.
			AnnotationAwareOrderComparator.sort(this.handlerMappings);
		}
		
		// Find all HandlerAdapters in the ApplicationContext, including ancestor contexts.
		Map<String, HandlerAdapter> matchingHanderlAdaptorBeans =
				BeanFactoryUtils.beansOfTypeIncludingAncestors(applicationContext, HandlerAdapter.class, true, false);
		if (!matchingHanderlAdaptorBeans.isEmpty()) {
			this.handlerAdapters = new ArrayList<HandlerAdapter>(matchingHanderlAdaptorBeans.values());
			// We keep HandlerAdapters in sorted order.
			AnnotationAwareOrderComparator.sort(this.handlerAdapters);
		}
	}

